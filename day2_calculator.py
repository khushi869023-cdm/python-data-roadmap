a = input ("enter first number : ")
b = input ("enter second number : ")
print("The sum is : ", int(a) + int(b))# simple addition calculator

print("The difference is : ", int(a) - int(b))# simple subtraction calculator
print("The product is : ", int(a) * int(b))# simple multiplication calculator
print("The quotient is : ", int(a) / int(b))# simple division calculator
print("The floor division is : ", int(a) // int(b))# simple floor division calculator
print("The modulus is : ", int(a) % int(b))# simple modulus calculator
print("The exponent is : ", int(a) ** int(b))# simple exponent calculator
